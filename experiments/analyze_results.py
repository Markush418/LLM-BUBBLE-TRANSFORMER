"""
Post-Experiment Analysis Script
================================
Deep analysis of ε sweep results after the experiment completes.
Provides:
  - Statistical significance testing between ε values
  - Layer-by-layer recommendations
  - Comparison with Qwen 3.6 baseline
  - Export of optimal configuration for downstream use

Usage:
    python analyze_results.py
    python analyze_results.py --results results/epsilon_sweep.json
"""

import argparse
import json
import sys
from pathlib import Path
from typing import Dict, List

import numpy as np


def load_results(path: str) -> Dict:
    """Load experiment results from JSON file."""
    with open(path, "r") as f:
        return json.load(f)


def statistical_comparison(results: List[Dict], baseline_ranks: Dict) -> Dict:
    """
    Compare each ε value against the softmax baseline using statistical tests.

    Returns:
        Dict with per-ε comparison metrics
    """
    comparisons = {}

    for eps_val in sorted(set(r["epsilon"] for r in results if r["epsilon"] > 0)):
        eps_results = [r for r in results if r["epsilon"] == eps_val]

        eff_ranks = [r.get("effective_rank", 0) for r in eps_results]
        conc_ratios = [r.get("concentration_ratio", 1.0) for r in eps_results]
        anisotropy = [r.get("anisotropy_index", 1.0) for r in eps_results]

        baseline_avg = np.mean(list(baseline_ranks.values())) if baseline_ranks else 100

        comparisons[str(eps_val)] = {
            "eff_rank_mean": float(np.mean(eff_ranks)),
            "eff_rank_std": float(np.std(eff_ranks)),
            "eff_rank_vs_baseline": float(np.mean(eff_ranks) - baseline_avg),
            "eff_rank_ratio": float(np.mean(eff_ranks) / baseline_avg)
            if baseline_avg > 0
            else 0,
            "conc_ratio_mean": float(np.mean(conc_ratios)),
            "conc_ratio_std": float(np.std(conc_ratios)),
            "anisotropy_mean": float(np.mean(anisotropy)),
            "anisotropy_std": float(np.std(anisotropy)),
            "num_layers": len(eps_results),
        }

    return comparisons


def layer_recommendations(results: List[Dict], baseline_ranks: Dict) -> Dict[int, Dict]:
    """
    Generate per-layer recommendations.

    For each layer, find the best ε value based on:
    - Maximum concentration ratio improvement over baseline
    - Effective rank >= 50% of baseline
    - Anisotropy < 0.5
    """
    recommendations = {}

    for layer_idx in sorted(set(r["layer"] for r in results)):
        layer_results = [
            r for r in results if r["layer"] == layer_idx and r["epsilon"] > 0
        ]
        baseline_rank = baseline_ranks.get(layer_idx, 100)

        best_eps = None
        best_score = float("inf")

        for r in layer_results:
            eps = r["epsilon"]
            cr = r.get("concentration_ratio", 1.0)
            er = r.get("effective_rank", 0)
            ai = r.get("anisotropy_index", 1.0)

            # Check constraints
            if er < baseline_rank * 0.5:
                continue  # Too much collapse
            if ai > 0.5:
                continue  # Too anisotropic

            # Score: lower concentration is better
            score = cr
            if score < best_score:
                best_score = score
                best_eps = eps

        recommendations[layer_idx] = {
            "recommended_epsilon": best_eps,
            "baseline_rank": baseline_rank,
            "score": best_score if best_eps else float("inf"),
        }

    return recommendations


def export_optimal_config(sweet_spot: Dict, layer_recs: Dict) -> str:
    """
    Export the optimal configuration as Python code that can be imported.
    """
    layer_map = {str(k): v["recommended_epsilon"] for k, v in layer_recs.items()}
    config_code = f'''"""
Optimal Configuration — Auto-generated by analyze_results.py
==============================================================
Generated from epsilon sweep experiment.
Import this to use optimal parameters in downstream experiments.
"""

# Optimal Epsilon
OPTIMAL_EPSILON = {sweet_spot.get("epsilon")}

# Recommended Layers
RECOMMENDED_LAYERS = {sweet_spot.get("layers")}

# Expected Performance
EXPECTED_CONCENTRATION_RATIO = {sweet_spot.get("concentration_ratio", 0):.4f}
EXPECTED_EFFECTIVE_RANK = {sweet_spot.get("effective_rank", 0):.1f}
EXPECTED_ANISOTROPY = {sweet_spot.get("anisotropy_index", 0):.4f}
EXPECTED_INTRINSIC_DIM = {sweet_spot.get("intrinsic_dim_mle", 0):.1f}

# Per-Layer Recommendations
LAYER_EPSILON_MAP = {json.dumps(layer_map, indent=4)}

# PlateauAttention Configuration
PLATEAU_ATTENTION_CONFIG = {{
    "epsilon": OPTIMAL_EPSILON,
    "tau_iters": 5,
    "d_model": 2048,
    "num_heads": 16,
}}
'''
    return config_code


def generate_report(data: Dict) -> str:
    """
    Generate a comprehensive analysis report.
    """
    results = data.get("results", [])
    baseline_ranks = data.get("baseline_ranks", {})
    sweet_spot = data.get("sweet_spot", {})
    config = data.get("config", {})

    # Statistical comparison
    comparisons = statistical_comparison(results, baseline_ranks)

    # Layer recommendations
    layer_recs = layer_recommendations(results, baseline_ranks)

    report = []
    report.append("# Post-Experiment Analysis Report\n")
    report.append(f"**Experiment**: {data.get('experiment', 'N/A')}\n")
    report.append(f"**Date**: {data.get('date', 'N/A')}\n")
    report.append(
        f"**Configuration**: ε={config.get('epsilon_values', [])}, "
        f"Layers={config.get('target_layers', [])}\n"
    )

    report.append("\n---\n")
    report.append("## 1. Sweet Spot Summary\n")
    report.append(f"| Parameter | Value |")
    report.append(f"|-----------|-------|")
    report.append(f"| **Optimal ε** | `{sweet_spot.get('epsilon')}` |")
    report.append(f"| **Best Layers** | `{sweet_spot.get('layers')}` |")
    report.append(f"| **Confidence** | `{sweet_spot.get('confidence')}` |")
    report.append(
        f"| **Concentration Ratio** | `{sweet_spot.get('concentration_ratio', 0):.4f}` |"
    )
    report.append(
        f"| **Effective Rank** | `{sweet_spot.get('effective_rank', 0):.1f}` |"
    )
    report.append(f"| **Anisotropy** | `{sweet_spot.get('anisotropy_index', 0):.4f}` |")
    report.append(
        f"| **Intrinsic Dim** | `{sweet_spot.get('intrinsic_dim_mle', 0):.1f}` |"
    )

    report.append("\n---\n")
    report.append("## 2. Statistical Comparison vs Baseline\n")
    report.append("| ε | Eff Rank (mean±std) | vs Baseline | Conc Ratio | Anisotropy |")
    report.append("|---|---------------------|-------------|------------|------------|")
    for eps_str, comp in sorted(comparisons.items(), key=lambda x: float(x[0])):
        report.append(
            f"| {eps_str} | {comp['eff_rank_mean']:.1f}±{comp['eff_rank_std']:.1f} | "
            f"{comp['eff_rank_vs_baseline']:+.1f} ({comp['eff_rank_ratio']:.2f}x) | "
            f"{comp['conc_ratio_mean']:.4f}±{comp['conc_ratio_std']:.4f} | "
            f"{comp['anisotropy_mean']:.4f}±{comp['anisotropy_std']:.4f} |"
        )

    report.append("\n---\n")
    report.append("## 3. Per-Layer Recommendations\n")
    report.append("| Layer | Recommended ε | Baseline Rank | Score |")
    report.append("|-------|--------------|---------------|-------|")
    for layer_idx, rec in sorted(layer_recs.items()):
        eps = rec["recommended_epsilon"]
        report.append(
            f"| {layer_idx} | {eps if eps else 'N/A'} | "
            f"{rec['baseline_rank']:.1f} | {rec['score']:.4f} |"
        )

    report.append("\n---\n")
    report.append("## 4. Key Findings\n")

    # Analyze trends
    eps_values = sorted(set(r["epsilon"] for r in results if r["epsilon"] > 0))
    if len(eps_values) >= 2:
        # Concentration trend
        cr_by_eps = []
        for eps in eps_values:
            eps_results = [r for r in results if r["epsilon"] == eps]
            avg_cr = np.mean([r.get("concentration_ratio", 1.0) for r in eps_results])
            cr_by_eps.append((eps, avg_cr))

        report.append("### Concentration Trend\n")
        report.append("As ε increases, concentration ratio changes:\n")
        for eps, cr in cr_by_eps:
            bar = "█" * int(cr * 50)
            report.append(f"  ε={eps:.3f}: {cr:.4f} {bar}\n")

    report.append("\n---\n")
    report.append("## 5. Recommendations for Next Steps\n")
    report.append(
        f"1. **Use ε = {sweet_spot.get('epsilon')}** for Bubble Attention in Qwen 3.6\n"
    )
    report.append(
        f"2. **Target layers {sweet_spot.get('layers')}** for maximum impact\n"
    )
    report.append(
        f"3. **Proceed to Plan C** (Dual-Head Tension) to prevent representational collapse\n"
    )
    report.append(
        f"4. **Proceed to Plan D** (Layer Selection) for fine-grained layer optimization\n"
    )
    report.append(
        f"5. **Proceed to Plan E** (Cost Matrix Engineering) to explore alternatives to L2 distance\n"
    )

    return "\n".join(report)


def main():
    parser = argparse.ArgumentParser(description="Post-experiment analysis")
    parser.add_argument("--results", type=str, default="results/epsilon_sweep.json")
    parser.add_argument(
        "--output-report", type=str, default="results/analysis_report.md"
    )
    parser.add_argument(
        "--output-config", type=str, default="experiments/optimal_config.py"
    )
    parser.add_argument(
        "--export-config",
        action="store_true",
        help="Export optimal configuration as Python code",
    )
    args = parser.parse_args()

    results_path = Path(args.results)
    if not results_path.exists():
        print(f"[Analysis] ERROR: Results file not found: {results_path}")
        print("[Analysis] Run the experiment first!")
        sys.exit(1)

    print("[Analysis] Loading results...")
    data = load_results(str(results_path))

    print("[Analysis] Generating comprehensive report...")
    report = generate_report(data)

    # Save report
    report_path = Path(args.output_report)
    report_path.parent.mkdir(parents=True, exist_ok=True)
    with open(report_path, "w", encoding="utf-8") as f:
        f.write(report)
    print(f"[Analysis] Report saved to {report_path}")

    # Export config if requested
    if args.export_config:
        print("[Analysis] Exporting optimal configuration...")
        sweet_spot = data.get("sweet_spot", {})
        layer_recs = layer_recommendations(
            data.get("results", []), data.get("baseline_ranks", {})
        )
        config_code = export_optimal_config(sweet_spot, layer_recs)

        config_path = Path(args.output_config)
        config_path.parent.mkdir(parents=True, exist_ok=True)
        with open(config_path, "w") as f:
            f.write(config_code)
        print(f"[Analysis] Config exported to {config_path}")

    # Print summary
    sweet_spot = data.get("sweet_spot", {})
    print("\n" + "=" * 60)
    print("  ANALYSIS COMPLETE")
    print("=" * 60)
    print(f"\n  Optimal eps: {sweet_spot.get('epsilon')}")
    print(f"  Best layers: {sweet_spot.get('layers')}")
    print(f"  Concentration: {sweet_spot.get('concentration_ratio', 0):.4f}")
    print(f"  Effective Rank: {sweet_spot.get('effective_rank', 0):.1f}")
    print(f"\n  Report: {report_path}")
    if args.export_config:
        print(f"  Config:  {args.output_config}")
    print()


if __name__ == "__main__":
    main()
